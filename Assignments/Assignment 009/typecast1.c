//WAP to convert float number to integer
#include<stdio.h>
int main()
{
    float a=14.459;
    int b;

    printf("\n\n\tTYPECASTING\n\t_______________________________\n\ta : %f",a);
    b=(int)a;
    printf("\n\tb : %d",b);

    printf("\n\n\n\tSuccessfully Executed..\n\n\n");
    return 0;
    }
